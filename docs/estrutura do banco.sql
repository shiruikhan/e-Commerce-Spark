-- ============================================================
-- TRIGGERS
-- ============================================================

-- tr_estoque_update_timestamp (pré-existente)
-- BEFORE UPDATE on estoque → atualiza dt_atualizacao automaticamente.

-- tr_log_pedido_status (pré-existente)
-- AFTER UPDATE on pedido → registra mudança de status em log_integracao_pedido.

-- tr_pedido_novo_integracao
-- AFTER INSERT on pedido → cria entrada em log_integracao_pedido com
-- status='pendente', tentativa=1 e payload_enviado com snapshot do pedido.
-- Garante rastreabilidade do pipeline de integração desde o momento da criação.

-- tr_log_integracao_tentativa
-- BEFORE UPDATE on log_integracao_pedido → quando status volta para
-- 'pendente' (retentativa), incrementa automaticamente o campo tentativa.

-- tr_log_sync_watchdog
-- BEFORE INSERT on log_sincronizacao → ao abrir novo ciclo 'processando'
-- para uma entidade, fecha automaticamente entradas anteriores presas
-- há mais de 5 minutos com status='erro_watchdog'.
-- Evita entradas eternas em 'processando' se a Edge Function for morta.

