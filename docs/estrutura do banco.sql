  CONSTRAINT estoque_pkey PRIMARY KEY (codprod),
  CONSTRAINT estoque_codprod_fkey FOREIGN KEY (codprod) REFERENCES public.produto(codprod)
);
CREATE TABLE public.log_integracao_pedido (
