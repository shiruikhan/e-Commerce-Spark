  codgrupopai bigint,
  CONSTRAINT categoria_pkey PRIMARY KEY (codgrupoprod),
  CONSTRAINT categoria_codgrupopai_fkey FOREIGN KEY (codgrupopai) REFERENCES public.categoria(codgrupoprod)
